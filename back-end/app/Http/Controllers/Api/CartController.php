    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Cart  $cart
     * @return \Illuminate\Http\Response
     */
    public function destroy($productId)
    {
        $item = Cart::where('product_id', $productId)->delete();

        return response(null, 200);
    }

